     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $categoryIds = $request->categoryIds;
        // Get Filter Product list based oncategory id
        $products = $this->productRepo->getProductListFromCategoryIds($categoryIds);

        $productHtml = view('partials.product_list')->with('products', $products)->render();
        return response()->json(
            [
                'status' => 200,
                'html' => $productHtml,
                'message' => 'Product list loaded Successfully'
            ]
        );
    }

